    private void go(String IP) {
        try {
            client = new Socket(IP, 2207);
            dos = new DataOutputStream(client.getOutputStream());
            dis = new DataInputStream(client.getInputStream());

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Kết nối thất bại, xin hãy kiểm tra lại IP.", "Message Dialog", JOptionPane.WARNING_MESSAGE);
            System.exit(0);
            
        }
    }

    private void sendMSG(String data) {
        try {
            dos.writeUTF(data);

            dos.flush();
        } catch (IOException e) {
            //e.printStackTrace();
        }
    }

    private String getMSG() {
        String data = null;
        try {
            data = dis.readUTF();
        } catch (IOException e) {
            //e.printStackTrace();
        }
        return data;
    }

    public void getMSG(String msg1, String msg2) {
        int stt = Integer.parseInt(msg1);
        switch (stt) {
            // tin nhắn của những người khác
            case 3:
                this.msg.append(msg2);

                break;
            // update danh sách online
            case 4:
                this.online.setText(msg2);

                break;
            // server đóng cửa
            case 5:
                dataStream.stopThread();
                exit();
                break;
            // bổ sung sau
            default:
                break;
        }
    }
//----------------------------------------------

    private void checkSend(String msg) {
        if (msg.compareTo("\n") != 0) {
            this.msg.append("Tôi : " + msg);
            sendMSG("1");
            sendMSG(msg);
        }
    }

    private boolean checkLogin(String nick) {
        if (nick.compareTo("") == 0) {
            return false;
        } else if (nick.compareTo("0") == 0) {
            return false;
        } else {
            sendMSG(nick);
            int sst = Integer.parseInt(getMSG());
            if (sst == 0) {
                return false;
            } else {
                return true;
            }
        }
    }

    private void exit() {
        try {
            sendMSG("0");
            dos.close();
            dis.close();
            client.close();
        } catch (IOException e) {
            //e.printStackTrace();
        }
        System.exit(0);
    }

    private void dangNhap() {
        if (checkLogin(txt_ten.getText())) {
            p_chat.setVisible(true);
            p_login.setVisible(false);
            txt_ten1.setText(txt_ten.getText());
            txt_ten1.setEditable(false);
            this.setTitle(txt_ten.getText());
            msg.append("Đã đăng nhập thành công\n");
            dataStream = new ThreadClien(this, this.dis);
        } else {
            JOptionPane.showMessageDialog(this, "Đã tồn tại níck này trong room, bạn vui lòng nhập lại.", "Message Dialog", JOptionPane.WARNING_MESSAGE);
        }
    }

