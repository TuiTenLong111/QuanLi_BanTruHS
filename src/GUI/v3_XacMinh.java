    private void btn_xacminhchukyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_xacminhchukyActionPerformed
        // TODO add your handling code here:
        String filename = txt_area_filecanxacminh_CHON.getText();
        filename = filename.replace('\\', '/');
        if ("".equals(filename)) {
            JOptionPane.showMessageDialog(null, "Hãy chọn file cần xác minh!", "Thông báo", JOptionPane.ERROR_MESSAGE);
        } else {
            SHA1 sha1 = new SHA1();
            try {
                // Tính toán mã băm SHA của file
                txt_area_SHA_xacminh.setText(sha1.md(filename).abs() + "");

                // Kiểm tra xem chuỗi chữ ký số có chứa ký tự không hợp lệ không
//                String chukysoDataoText = txt_area_chukyso_datao.getText().trim().replaceAll("\\s+", "");
//                if (!chukysoDataoText.matches("\\d+")) {
//                    JOptionPane.showMessageDialog(null, "Chữ ký số không hợp lệ!", "Lỗi", JOptionPane.ERROR_MESSAGE);
//                    return; // Kết thúc phương thức nếu chữ ký số không hợp lệ
//                }
                String chukysoDataoText = txt_area_chukyso_datao.getText().trim();

                // Tìm vị trí của ký tự 'F' trong chuỗi chữ ký số.
                int indexF = chukysoDataoText.indexOf('F');

                String sign = chukysoDataoText.substring(0, indexF).trim().replaceAll("\\s+", "");
                String modul = chukysoDataoText.substring(indexF + 1).trim().replaceAll("\\s+", "");

                System.out.println("sign: " + sign);
                System.out.println("\nmodul : " + modul);

                BigInteger dsrsa = new BigInteger(sign);
                BigInteger m = new BigInteger(modul);
                BigInteger publicKey = new BigInteger(txt_area_khoacongkhai_CHON.getText().trim());
                rsa.setModul(m);
                rsa.setKhoa_cong_khai(publicKey);

                // Giải mã chữ ký số bằng thuật toán RSA, sử dụng phương thức giai_ma
                txt_area_chukyso_XACMINH.setText(rsa.v1_giaima(dsrsa, rsa).toString());

                /* 
                So sánh hai chuỗi: mã băm SHA và chữ ký số giải mã
                + Nếu giống -> chữ ký số không thay đổi
                + Nếu không giống nhau -> chữ ký số đã bị thay đổi
            
                 */
                if (txt_area_SHA_xacminh.getText().equals(txt_area_chukyso_XACMINH.getText())) {
                    JOptionPane.showMessageDialog(null, "Chữ ký số không thay đổi!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(null, "Chữ ký số đã bị thay đổi! File đã bị chỉnh sửa.", "Thông báo", JOptionPane.ERROR_MESSAGE);
                }
            } catch (Exception ex) {
                Logger.getLogger(v3_XacMinh.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btn_xacminhchukyActionPerformed

