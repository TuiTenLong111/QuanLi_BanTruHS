    private void btn_thuHoiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_thuHoiActionPerformed
        // TODO add your handling code here:
        String tenBang = cb_thaoTac.getSelectedItem().toString().trim();
        String manv = txt_username.getText().trim();

        if (ckb_select.isSelected() || ckb_update.isSelected() || ckb_insert.isSelected() || ckb_delete.isSelected()) {
            if (ckb_select.isSelected()) {
                NhanVien_DAO.thuHoiQuyen("SELECT", tenBang, manv);
            }
            if (ckb_update.isSelected()) {
                NhanVien_DAO.thuHoiQuyen("UPDATE", tenBang, manv);
            }
            if (ckb_insert.isSelected()) {
                NhanVien_DAO.thuHoiQuyen("INSERT", tenBang, manv);
            }
            if (ckb_delete.isSelected()) {
                NhanVien_DAO.thuHoiQuyen("DELETE", tenBang, manv);
            }
            JOptionPane.showMessageDialog(this, "Thực hiện chức năng thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn ít nhất một chức năng", "Thông báo", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btn_thuHoiActionPerformed

