    public ArrayList<DBA_SYS_PRIVS> LoadDuLieuQuyenNV() {
        ArrayList<DBA_SYS_PRIVS> dsp = new ArrayList<DBA_SYS_PRIVS>();
        String masv = txt_username.getText();
        try {          
            String sql = "SELECT GRANTEE, OWNER, TABLE_NAME, GRANTOR, PRIVILEGE  FROM USER_TAB_PRIVS WHERE GRANTEE = '"+ masv +"' AND GRANTEE != 'SYS' AND PRIVILEGE IN ('SELECT', 'UPDATE', 'DELETE', 'INSERT')";
            DBConnect conn = new DBConnect();
            conn.GetConnect();           
            ResultSet rs = conn.executeQuery(sql);           
            while (rs.next()) {  
            String GRANTEE = rs.getString("GRANTEE");
            String OWNER = rs.getString("OWNER");
            String TABLE_NAME = rs.getString("TABLE_NAME");
            String GRANTOR = rs.getString("GRANTOR");
            String PRIVILEGE = rs.getString("PRIVILEGE");

            DBA_SYS_PRIVS ds = new DBA_SYS_PRIVS(GRANTEE, OWNER, TABLE_NAME, GRANTOR, PRIVILEGE);
            dsp.add(ds);
            System.out.println("Lay du lieu nhan vien thanh cong!");
        }             
        } catch (Exception e) {
            //System.err.println("Lay du lieu nhan vien that bai!");
            e.printStackTrace();
        }
        return dsp;
    }
