    public String layNgayHeThong() {
        // Lấy ngày hiện tại từ hệ thống
        Date currentDate = new Date();
        // Xác định định dạng ngày tháng mặc định của hệ thống
        DateFormat systemDateFormat = DateFormat.getDateInstance();
        // Định dạng ngày thành chuỗi với định dạng mặc định của hệ thống
        String formattedDate = systemDateFormat.format(currentDate);
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String ngayHeThong = dateFormat.format(currentDate);
        return ngayHeThong;
    }

