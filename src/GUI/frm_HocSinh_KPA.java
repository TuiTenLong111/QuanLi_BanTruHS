    // load ds khẩn phần mặn
    public void showDuLieu_ComBoMonAnHienThi_KPMan(ArrayList<POJO_Combomonan> dscbhtman) {
        dscbhtman = DAO_KhauPhanAn.getDSComBoMonAnHienThi_Man();

        tblDataman.removeAllElements();
        for (POJO_Combomonan b : dscbhtman) {
            Vector v = new Vector();
            v.add(b.getMaComBoMon());
            v.add(b.getTenMon1());
            v.add(b.getTenMon2());
            v.add(b.getTenMon3());
            v.add(b.getTenMon4());
            v.add(b.getLoaiComBoMon());
            v.add(b.getThuTrongTuan());
            v.add(b.getGiaTien());
            v.add(b.getQRCode());
            tblDataman.add(v);
        }
        jTable_combomonanman.setModel(new DefaultTableModel(tblDataman, tblTitleman));
    }

